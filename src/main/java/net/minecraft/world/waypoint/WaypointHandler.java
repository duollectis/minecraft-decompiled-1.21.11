package net.minecraft.world.waypoint;

/**
 * Слушатель жизненного цикла вейпоинта на клиенте.
 * <p>
 * Вызывается клиентским кодом при получении пакетов от сервера:
 * начало отслеживания, обновление данных и прекращение отслеживания.
 *
 * @param <T> конкретный тип вейпоинта
 */
public interface WaypointHandler<T extends Waypoint> {

	/** Вызывается при начале отслеживания нового вейпоинта. */
	void onTrack(T waypoint);

	/** Вызывается при получении обновлённых данных уже отслеживаемого вейпоинта. */
	void onUpdate(T waypoint);

	/** Вызывается при прекращении отслеживания вейпоинта. */
	void onUntrack(T waypoint);
}
