package net.minecraft.world.entity;

/**
 * Обработчик жизненного цикла сущности в системе управления сущностями.
 * Вызывается менеджером сущностей при изменении состояния отслеживания или тикинга.
 *
 * @param <T> тип сущности
 */
public interface EntityHandler<T> {

	/** Вызывается при создании сущности в менеджере (первичная инициализация). */
	void create(T entity);

	/** Вызывается при уничтожении сущности (финальная очистка). */
	void destroy(T entity);

	/** Вызывается, когда секция сущности переходит в состояние TICKING. */
	void startTicking(T entity);

	/** Вызывается, когда секция сущности выходит из состояния TICKING. */
	void stopTicking(T entity);

	/** Вызывается, когда сущность начинает отслеживаться (секция TRACKED или TICKING). */
	void startTracking(T entity);

	/** Вызывается, когда сущность перестаёт отслеживаться. */
	void stopTracking(T entity);

	/** Вызывается при изменении статуса загрузки секции, в которой находится сущность. */
	void updateLoadStatus(T entity);
}
