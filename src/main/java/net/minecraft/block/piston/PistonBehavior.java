package net.minecraft.block.piston;

/**
 * Определяет поведение блока при взаимодействии с поршнем.
 * <p>
 * Используется в {@link net.minecraft.block.PistonBlock} для принятия решений
 * о том, можно ли переместить, уничтожить или проигнорировать блок.
 */
public enum PistonBehavior {
	/** Блок перемещается поршнем в обычном режиме. */
	NORMAL,
	/** Блок уничтожается при попытке поршня его сдвинуть. */
	DESTROY,
	/** Блок блокирует движение поршня и не может быть сдвинут. */
	BLOCK,
	/** Поршень игнорирует блок — проходит сквозь него. */
	IGNORE,
	/** Блок может быть только вытолкнут поршнем, но не притянут. */
	PUSH_ONLY;
}
