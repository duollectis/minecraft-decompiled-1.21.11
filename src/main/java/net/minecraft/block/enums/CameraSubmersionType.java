package net.minecraft.block.enums;

/**
 * Тип погружения камеры игрока в среду.
 * Определяет, в какой жидкости или материале находится точка зрения игрока,
 * что влияет на визуальные эффекты наложения экрана.
 */
public enum CameraSubmersionType {
	/** Камера погружена в лаву. */
	LAVA,
	/** Камера погружена в воду. */
	WATER,
	/** Камера погружена в порошковый снег. */
	POWDER_SNOW,
	/** Камера находится в воздухе (атмосфере). */
	ATMOSPHERIC,
	/** Камера не погружена ни в какую среду. */
	NONE;
}
